using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;

namespace Problem5
{
    class Scout
    {
        // Private DATA variables
        private string name;
        private int troop;
        private double dues;
        public static string Motto = "To Obey the Girl Scout Law";

        // Public properties that manipulate data variables
        public string Name
        {
            get
            {
                return Name;
            }

            set
            {
                Name = value;
            }
        }

        public int Troop
        {
            get
            {
                return Troop;
            }

            set
            {
                Troop = value;
            }
        }
        public double Dues
        {
            get
            {
                return Dues;
            }

            set
            {
                Dues = value;
            }
        }
        
    }
}
